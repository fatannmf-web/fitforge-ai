// Build lookup map for O(1) access
const EXERCISE_MAP: Record<string, ExerciseData> = {};
LIBRARY.forEach(ex => { EXERCISE_MAP[ex.id] = ex; });

// Fuzzy name matching (case insensitive, partial)
export function findExercise(name: string): ExerciseData | undefined {
  const lower = name.toLowerCase().replace(/[^a-z0-9 ]/gi, "");
  // Exact id match
  if (EXERCISE_MAP[lower.replace(/ /g, "-")]) return EXERCISE_MAP[lower.replace(/ /g, "-")];
  // Name match
  return LIBRARY.find(ex => {
    const exLower = ex.name.toLowerCase();
    const roLower = (ex.nameRo || "").toLowerCase();
    return exLower.includes(lower) || lower.includes(exLower) ||
           roLower.includes(lower) || lower.includes(roLower);
  });
}

export function getExercisesByCategory(category: ExerciseData["category"]): ExerciseData[] {
  return LIBRARY.filter(ex => ex.category === category);
}

